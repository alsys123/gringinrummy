// Helper to build cards
function C(rank, suit) {
//  const values = { A:1, J:10, Q:10, K:10 };
  const values = { A:1, J:11, Q:12, K:13 };
  return {
    rank,
    suit,
    value: values[rank] || Number(rank)
  };
}

